<h1>Blood (or Whole Blood) Donation
</h1>Whole Blood Donation	 	
This is the most common type of donation, during which approximately a pint of 'whole blood' is given.</br></br>
The blood is separated into transfusable components – red cells, plasma, platelets and/or cryoprecipitated AHF.</br></br>
This type of blood donation usually takes about an hour, though the actual donation takes about 8-10 minutes.

</br>
You are eligible to donate 'whole blood' every 56 days.
